<h4>Un traitement par la méthode McKenzie, ou Diagnostic et Thérapie Mécanique (MDT), est une prise en charge spécifique. Cette prise en charge ne peut se confondre avec autre méthode de rééducation.</h4>
<h4>Tous les <a href="03a_praticiens.php">praticiens</a> dont les coordonnées figurent sur le site de l'Association Française McKenzie s'engagent à respecter les règles suivantes et à afficher la Charte dans leur salle d'attente.<br />(La Charte est téléchargeable dans la partie réservée aux adhérents, sous l'onglet "Divers")</h4>


<table><tr><td><ul>
    <li class="liste3"><strong>Une prise en charge individuelle et individualisée.</strong></li>
    <li class="liste3"><strong>La réalisation systématique d’un bilan MDT au premier rendez-vous, afin de définir le traitement.</strong> Ce bilan comprend notamment un interrogatoire exhaustif, la réalisation de mouvements répétés ou de postures de fin d’amplitude, ainsi que l’analyse de leurs effets sur la localisation et l'intensité de la douleur, et sur la mobilité articulaire. Il nécessite du temps et une compétence (validée par la certification).</li>
    <li class="liste3"><strong>La conduite du traitement basé essentiellement sur la réalisation de mouvements spécifiques, de postures et/ou sur un contrôle postural, déterminés par le bilan.</strong></li>
    <li class="liste3"><strong>Une réévaluation de l'état du patient à chaque séance et le réajustement au besoin du traitement.</strong></li>
    <li class="liste3"><strong>Privilégier l'auto-traitement, guidé par le bilan et les réévaluations.</strong></li>
    <li class="liste3"><strong>L’utilisation de techniques manuelles (surpressions, mobilisations, manipulations) avec parcimonie : Potentiellement, elles créent une dépendance au thérapeute.</strong> Elles sont cependant parfois indispensables, ponctuellement, à certains stades du traitement.</li>
    <li class="liste3"><strong>Le massage et la physiothérapie</strong> (électro-thérapie, ultrasons, infrarouges...) <strong>ne faisant pas partie de la méthode McKenzie, leur utilisation éventuelle ne se fera qu'en tant qu'adjuvant ponctuel.</strong></li>
    <li class="liste3"><strong>Donner le plus d'autonomie possible au patient, ainsi que les informations et les moyens indispensables pour :</strong></li>
    </ul><ul class="liste2">
         <li>Pérenniser le résultat obtenu.</li>
         <li>Savoir agir face à une éventuelle rechute.
