            <h2 id="pourquoi">Pourquoi faire ?</h2>

            <h3>Les intérêts de passer l'épreuve</h3>
            <div class="bs-callout bs-callout-info">
                <p>Passer l'épreuve présente plusieurs intérêts</p>
            </div>
            <p>Tout d'abord il s'agit d'une épreuve standardisée identique quelque soit le pays où elle se déroule.
                Ce système de certification internationale permet à la fois une reconnaissance similaire de l'épreuve
                dans tous les pays où elle a lieu mais également de conserver un niveau international homogène de
                pratique professionnelle. C'est donc aussi, de fait, la possibilité qu'un jour cette certification soit
                reconnue en France comme une spécialité (ou « qualification »)et/ou spécificité(c'est un des
                objectifs important de l'AFMcK voir l'article à ce sujet dans l'onglet « l'actualité de l'association »).</p>

            <p>Directement lié à cette notion de niveau international de pratique homogène , les praticiens
                certifiés peuvent, s'ils le désirent, participer à des études et permettre de faire progresser la
                connaissance dans de nombreux domaines en relation avec les pathologies musculo-squelettiques.</p>

            <p>C'est aussi une façon de terminer un cursus complet de séminaires et de s'assurer personnellement
                d'être effectivement au niveau.</p>

            <p>Passer cette épreuve est une façon aussi de promouvoir l'approche MDT : en effet, plus le nombre
                de praticiens certifiés sera important en France, plus cette méthode à des chances d'être reconnue,
                appréciée et pratiquée correctement. La bonne pratique et donc un haut niveau de formation sont
                autant d'arguments forts en faveur d'une reconnaissance plus large de la part aussi bien du monde
                médical que des organismes payeurs.</p>

            <p>Enfin pour tous ceux qui se sont soumis à cette épreuve c'est surtout l'occasion de reprendre point
                par point les différents aspects de cette méthode : réussite ou pas à la certification, la très grande
                majorité des candidats qui se sont présentés à l'épreuve assurent avoir modifié et affiné leur pratique
                quotidienne suite aux révisions.</p>

            <h2 id="prepa">Préparation à l'examen</h2>
            <div class="bs-callout bs-callout-info">
                <p>Une fois décidé et inscrit, c'est le moment de se lancer dans les révisions pour la certification ! </p>
            </div>
            <p>Le plus simple mais aussi le plus formateur c'est de reprendre les points clefs de la méthode et
                mettre en ordre ses notes personnelles prises durant les séminaires. Faire des fiches semble être une
                bonne façon de procéder.</p>

            <p>La lecture des différents manuels destinés au patients (« traiter vous même votre dos » et « traiter
                vous même votre cou » disponibles sur différents sites dont les liens sont en page d accueil) peut se
                révéler être une bonne chose pour recadrer les différentes notions en MDT.</p>

            <p>De la même façon l'article des EMC (encyclopédie médico-chirurgicale) paru en février 2011 et
                disponible sur cette page peut être d'une grande aide méthodologique.</p>

            <p>Le séminaire de préparation à la certification proposé par l'institut McKenzie France est important
                notamment pour se familiariser avec le type de questions sur l'épreuve pratique et parce qu'il permet
                de faire des révisons précises des différentes techniques indispensables. Il vous sera également
                proposé lors de ce we des exemples de questions avec réponse à choix multiples qui vous
                permettrons de vous familiariser avec la méthodologie qu'il faut adopter lors d'épreuve type QCM.</p>

            <p>Un écueil souvent décrit par les participants malheureux à l'épreuve de certification consiste à
                penser qu'il n'est pas nécessaire de vraiment approfondir les choses puisque «je pratique tous les
                jours McKenzie dans mon cabinet et donc je dois arriver sans problèmes à avoir cette épreuve».
                Un exemple qu'on peut garder en tête pour illustrer cette écueil serait le suivant : «combien de
                conducteur avec 10 ans de conduite sans accrochages auraient leur code du premier coup sans
                préparation ?» Quelques un certainement mais pas la majorité ! Pour cette épreuve il faut donc
                rouvrir vos manuels ...</p>

            <p>Disponible en bas de cette page les corrections de tous les « quizz » des manuels A et B . Les
