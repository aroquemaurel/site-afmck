            <h2 id="conseils">Conseils pour le jour J</h2>
            <h3>L'épreuve du matin (3h)</h3>
            <p>Elle commence par les 50 QCM et dure une heure trente, vous avez donc un peu moins de 2mn par question.<br/>Vous serez installés seul à une table sans aucun effet personnel : on vous fournit crayon, feuille de brouillon, verre pour boire.<br/>L’examen ne comporte pas de point négatif et pour chaque question il n’y a qu’une réponse correcte.</p>

            <div class="alert alert-warning">
                <p><strong>N'oubliez pas</strong> </p>
            <ul>
                <li>Ne restez pas trop longtemps coïncés sur une question.<br />Notez son numéro et continuez, vous y reviendrez plus tard</li>
                <li>Procédez par élimination</li>
                <li>Faites preuve de bon sens en vous mettant en situation</li>
                <li>N’hésitez pas à relire plusieurs fois la question</li>
            </ul>
            </div>

            <p>Suit l’épreuve QCM des fiches d’examen et études de cas.</p>
            <p>Vous finissez sur des QCM en rapport avec le matériel utilisé dans la méthode McKenzie.</p>


            <h3>L'épreuve de l'après-midi</h3>
            <p>Vous serez divisés en 2 groupes : pratique et vidéo.</p>

            <h4>La vidéo : 1h30</h4>
            <p>Vous allez assister à un examen et une proposition de traitement avec un patient et un examinateur, le choix du sujet est tiré au sort . L’examinateur ne fera pas forcément son travail : soyez attentifs et concentrés.</p>
            <p>Vous visualisez une séquence (interrogatoire puis bilan puis traitement ), elle est interrompue et vous devez répondre sur une feuille que l’on vous distribue à des questions type QCM, vous rendez la feuille et la vidéo reprend.</p>

            <h4>La pratique: 15mn</h4>
            <p>Au moment des révisions, vous sera remise une documentation sur les techniques à connaître le jour J.</p>
            <p>3 techniques vous seront demandées : un examinateur vous observe et apprécie votre placement, celui du patient, celui de vos mains, votre rythme etc et un autre joue le rôle du patient et apprécie « en direct » votre manoeuvre.</p>
