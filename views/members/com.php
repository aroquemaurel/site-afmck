    <h2 id="factuhn">La facturation HN</h2>
    <div class="factuhn">

        <div class="bs-callout bs-callout-warning">
        La facturation Hors-Nomenclature (HN) est une question récurrente lors des cours et des échanges avec les thérapeutes qui pratiquent l'approche McKenzie au quotidien. L'association a cherché à trouver un moyen de répondre à cette question.</h2>
        </div>

        <div class="bs-callout bs-callout-warning">
        Pour le moment et en l'état actuel de notre nomenclature,<br />le plus judicieux semble de pratiquer un acte HN pour le bilan plutôt qu'un dépassement d'honoraire à chaque séance. <br />Bien sûr chacun est libre de procéder comme il l'entend !</h1>
        </div>

        <div class="bs-callout bs-callout-warning">
            Les propositions qui suivent ne sont qu'indicatives ! Elles reflètent les pratiques des thérapeutes qui facturent une tarification particulière dès lors qu'ils utilisent l'approche McKenzie.
        </div>

        <p>La première chose à faire dans un cabinet libéral est d'informer le patient. <strong>C'est une obligation</strong>.</p>
        <p><a href="../pgadh/docdwld/02c-lacom/facturhn/affiche cabinet HN_AFMcK.pdf">
                <i class="glyphicon glyphicon-download-alt"></i> Honoraires à afficher en salle d'attente</a></p>

        <p>Concrètement il faut en informer le patient dès la prise de RDV bien sûr mais cela doit aussi être consultable par écrit dans votre cabinet.</p>
        <p>Sur le lien ci-contre, un texte vous est proposé : (à afficher dans votre salle d'attente, typiquement  sur le panneau information de votre cabinet)</p>
        <p>A nouveau, il ne s'agit que d'une propositon de formulation. Vous êtes libres de l'utiliser en intégralité ou seulement en partie selon vos besoins et le type d'activité que vous pratiquez au cabinet.</p>
        <p>Il est évident que chaque praticien reste libre de pratiquer les honoraires qu’il souhaite.</p>
        <p>Entre 20 et 50 euros pour la réalisation du bilan McKenzie International et du traitement avec la méthode McKenzie semble actuellement être la fourchette standard de tarification pour les certifiés en France.</p>

        <p>L'association vous propose ci-dessous un modèle de facturation HN à remettre à vos patients à la fin de la première séance bilan McKenzie:</p>
        <p><a href="../pgadh/docdwld/02c-lacom/facturhn/Facture HN_AFMcK.pdf"><i class="glyphicon glyphicon-download-alt"></i> Modèle de facture HN</a></p>

        <p>Ce tarif vient en supplément des honoraires conventionnés. Il s'intègre dans notre comptabilité professionnelle  à la ligne « Honoraires » et est donc soumis au calcul de l'URSSAF (voir avec votre comptable pour les détails).</p>
        <p>En fonction du type de contrat souscrit par le patient auprès de sa mutuelle tout ou partie de ce montant HN peut être pris en charge par cette dernière.</p>

        <div class="alert alert-warning">
        <p><strong>Attention</strong>: le bilan McKenzie International ne se substitue pas au bilan conventionné.<br />Le bilan conventionné est obligatoire dès la première séance et à ce titre  doit également être établi. Il n'est facturable qu'à partir de la 10ème séance. Les modèles standards de ce bilan sont disponibles sur les logiciels de gestion de patientèle.  La fiche synthèse de ce bilan conventionné doit être tenue à disposition du médecin conseil de la Sécurité Sociale et envoyée au médecin prescripteur (pour toute prescription de 10 séances au moins).<br />L'association propose <a href="#synthbil">un bilan spécifique</a> à envoyer aux médecins prescripteurs.</p>
        </div>
        <p>L'association vous propose aussi dans ce cadre, des fichiers téléchargeables de bilans types, avalisés par la sécurité sociale pour les pathologies soumisent à référentiel (DAP)</p>
