<h2>POURQUOI SE FORMER ?</h2>   
   <h4><strong>Se former est indispensable pour pratiquer correctement avec cette approche.</strong></h4>
   <h4>L'apparente « simplicité » des techniques à la consultation des différents ouvrages libres d'accès ne donne qu'une image très partielle de la pratique réelle avec un patient. <h4/>
   <h4>Principalement l'arborescence décisionnelle ne peut être appréhendée correctement qu'au travers d'une formation. De nombreux exemples de mauvaises  pratiques existent de par le monde : certains praticiens ayant simplement consultés les ouvrages de références sans avoir suivis de formation spécifiques ont aggravés parfois durablement leur patients allant parfois jusqu'à remettre en cause la méthode elle-même. </h4>
   <h4><strong>En tant que patient potentiel</strong> de cliniciens exerçants avec cette approche, il est vivement conseillé de vérifier au préalable le niveau de formation de votre thérapeute. Sur le site de l'AFMcK, vous avez accès à une liste mise à jour régulièrement de praticiens formés.</h4>
   <h4>Retenez qu'un praticien ayant effectué au moins la partie C pourra vous aider pour les problèmes à la fois lombaires cervicaux et dorsaux. Évidemment un praticien certifié ayant signé la charte de bonne pratique est un gage supplémentaire de sécurité et d'efficience dans cette approche.</h4>
   <h4><strong>En tant que professionnel</strong> la formation suivie et l'adhésion à notre association vous permettront d'augmenter votre efficacité en toute sécurité dans votre pratique quotidienne ainsi que votre visibilité sur une région d'exercice donnée. Cette démarche nécessite un engagement personnel de professionnel de santé seul capable de garantir un haut niveau de compétence dans votre pratique quotidienne. </h4>

