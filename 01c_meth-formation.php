<!-------- COMMENT SE FORMER -------->
    <h2>COMMENT SE FORMER ?</h2>   
   <h4>Il existe de nombreux instituts de formation dans le monde.<br />Toutes les formations proposées à travers le monde ont la même valeur : chacun peut donc choisir en fonction de sa langue maternelle ou de ses disponibilités, un lieu de formation.</h4>
   <h4>Le site <a href="http://www.mckenziemdt.org" target="_blank">McKenzie International</a> donne les liens vers toutes les branches nationales existantes:</h4>
   <h4>Chaque institut national possède sa branche d'enseignement : pour la France l'institut est basé à Blagnac (à côté de Toulouse) et des cours ou des présentations sont proposés à travers toute la France (Plus de 10 lieux en France y compris La Réunion). <br />
Le planning précis ville par ville et/ou date par date est disponible sur le <a href="http://www.mckenzie.fr" target="_blank">site de l'institut.</a></h4>
   <h4>Le coût de la formation est également précisé sur ce site. La formation est prise en charge en France au titre du FIF.PL dans les conditions habituelles pour ce type de formations.</h4>

   <h4>La formation se décompose en 4 modules différents : modules A,B,C et D.</h4>

   <h4>La composition précise de l'enseignement de chacun de ces modules est précisé sur le site français. </h4>

   <h4>A l'issu du module D les praticiens qui le souhaitent peuvent passer le certificat international M.D.T(Cert.M.D.T)en France (à Paris en général) organisé en moyenne une fois par an. </h4>

   <h4>Avec le certificat M.D.T il est ensuite possible de poursuivre la formation : <br />celle ci s'effectue exclusivement en langue anglaise et nécessite de ce fait une très bonne pratique de la langue (un pourcentage de 80% de réussite au TOEFL est obligatoire). Elle consiste à parfaire les connaissances théoriques et cliniques (diplôme universitaire par correspondance en Nouvelle Zélande et clinicat de plusieurs semaines aux Etats-Unis en nouvelle Zélande ou en Écosse).</h4>
   <h4>Cette formation est sanctionnée par le passage du diplôme M.D.T (Dip.M.D.T) qui ouvre à l'enseignement M.D.T.</h4>
   
