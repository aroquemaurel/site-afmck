    <h2>Evaluation et traitement d’une épaule via la methode McKENZIE: Etude du cas Clinique de Mr J</h2>
    <h3>INTERROGATOIRE :</h3>
   <p>Mr J 32 ans, chauffeur routier en reconversion professionnel, vient consulter pour des douleurs à  l’épaule droite présentes depuis 2 mois qui se sont aggravées en 15 jours. Il est en arrêt de travail car il ne peut plus se servir de son épaule et a stoppé toute activité de loisirs. L’EVA est de 8/10 lors des mouvements et 3/10 au repos. Rien ne semble être à l’origine de la douleur qui progressivement s’est accentuée devenant constante au cours de la journée et l’empêchant de dormir, ne trouvant pas de position indolore.</p>
   <p>La seule chose qui soulage l’épaule de Mr J c’est le repos strict, assis.</p>
   <p>La radiographie ne montre rien de particulier, la prise d’antalgique et d’AINS semble soulager quelques heures. Pour pouvoir dormir le patient prend des somnifères. Aucun drapeau rouge n’est présent.</p>
   
   <h3>EXAMEN PHYSIQUE :</h3>
   <p>L’épaule est limitée  en actif à 20°de flexion, 35° d’Abduction, pas de possibilité de rotation externe, ni d’Adduction horizontale. L’extension et la rotation interne sont intactes. En passif on monte à 90°de flexion et d’Abduction.</p>
   <p>Au niveau de la nuque on note un maintien en protrusion, avec des limitations importantes en rétraction, extension, rotation et inclinaison droite. Les douleurs sont en fin d’amplitudes et abolies au retour en position initiale.</p>
   <p>La répétition des mouvements au niveau de l’épaule est impossible  pour le patient car trop douloureux. Les rétractions répétées permettent un gain d’amplitude au niveau de la nuque dans le plan sagittal mais restent sans effet sur l’épaule.</p>
   
   <h3>CONCLUSION :</h3>
   <p>Du fait des douleurs importantes du patient au niveau de l’épaule, il est impossible actuellement de travailler les mouvements répétés. Je choisi de travailler les cervicales qui présentent  des limitations afin de voir si une corrélation est présente pour ce patient. </p>
   
   <h3>TRAITEMENT A DOMICILE :</h3>
   <p>Répétitions de 10 rétraction  toutes les 2h. Moins si le patient ressent une augmentation de ses douleurs dans l’épaule.</p>
   
   <h3>SEANCE N°2 :</h3>
   <p>Amplitudes de l’épaule inchangées, mais des douleurs au repos qui par intermittence disparaissent. Les amplitudes de rétraction, extension  ont augmenté, mais pas de modifications pour l’inclinaison et la rotation. Durant la séance, augmentation de la force par des rétractions/extension et des surpressions du patient en fin de la séance. On obtient une modification des amplitudes dans le plan sagittal et frontal.  On revoit aussi la posture assise.</p>
   
   <h3>SEANCE N°3 :</h3>
   <p>Les douleurs au repos sont quasi nulles, mais toujours des douleurs la nuit lors des changements de positions. Les amplitudes articulaires de l’épaule ont augmentés : 50° de flexion, d’abduction, mais la rotation externe est toujours impossible. Au niveau cervical on note  une douleur de fin d’amplitude avec un craquement lors de la rétraction/extension. La rotation et l’inclinaison droite sont améliorées.</p>
   <p>Traitement durant la séance par des surpressions du thérapeute en rétraction. Ce qui permet un gain complet en rotation et inclinaison et une augmentation à 90° de flexion et l’abduction reste à 50°.</p>
   
   <h3>SEANCE N°4 :</h3>
   <p>Le patient a pris un analgésique puissant qui lui a permis de ne plus avoir de douleur lors des changements de position  la nuit. Actuellement les mouvements de l’épaule sont indolores mais restent limités à 90° de flexion (actif ou passif) 50°d’ abduction active et 90° en passif. La rotation externe, n’a pas évoluée et reste impossible. </p>
   <p>Les amplitudes cervicales sont complètes  et indolores.</p>
   <p>Au niveau du traitement, des adductions horizontales sont réalisées permettant une augmentation significative de la flexion passant à 120° et abduction à 90° en actif.</p>
      
   <h3>SEANCE N°5 :</h3>
   <p>Augmentation de l’abduction à 120° et la flexion à 160° . Rotation externe possible mais limitée.</p>
   <p>La surpression du patient en Adduction horizontale ne permet pas de progression, mais les surpressions en flexion permettent un gain non maintenu de flexion et de rotation externe.</p>
   
   <h3>SEANCE N°6 :</h3>
   <p>Grace à la surpression en flexion on obtient une flexion complète, une augmentation légère de la rotation externe mais une abduction qui n’a pas augmentée.</p>
   
   <h3>EVOLUTION DU TRAITEMENT :</h3>
   <p>Durant les  5 séances suivantes, les évolutions articulaires ont été lentes. Un retour à des mouvements répétés en adductions avec surpression en alternance avec des surpressions en flexion ont été nécessaires. </p>
   <p>Des douleurs se sont faites de nouveau ressentir au niveau de l’épaule lors des mouvements, elles ont été contrées par des rétraction- extensions cervicales et extension dorsales haute.</p>
   <p>Après deux mois de traitement à raison de 2 séances par semaine suite aux  5 séances rapprochées, les amplitudes complètes ont été obtenues et un travail de renforcement (plus spécifique au niveau des rotateurs externes) a permis de retrouver une force quasi identique à avant les symptômes selon le patient.  </p>
    
