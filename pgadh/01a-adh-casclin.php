       <h2>Etude du cas Clinique de Mr T</h2>
   <h3>INTERROGATOIRE :</h3>
   <p>Monsieur  T 44 ans est envoyé par son généraliste pour une tendinite rotulienne à droite. Il s’agit d’un chaudronnier-tôlier qui travaille devant une machine avec le pied gauche sur la pédale pour faire défiler les plaques de tôle ; il est fléchi en avant afin de travailler le métal. Monsieur T est plutôt sédentaire en dehors du travail : canapé, un peu de bricolage qu’il a dû arrêter à cause de l’épisode douloureux coté à 4/10 sur l’EVA de façon constante et 7/10 au travail.</p>
    <p>Les douleurs au niveau de la face latérale du genou droit sont présentes depuis 3 semaines sans raison apparente et restent stationnaires.</p>
    <p>IL y a un mois et demi, le patient a présenté des symptômes de lombalgie et pseudo-sciatique pendant une semaine, avant que ceux-ci ne disparaissent totalement. Depuis 2000, il présente des antécédents récurrents de lombalgie soulagée par des séances d’ostéopathie.  Pour les douleurs au niveau du genou, le patient a effectué des séances de kinésithérapie (à base de massage, d’étirements, d’ultrasons et de mobilisation) qui n’ont rien modifié.</p>
    <p>La douleur à l’intérieur du genou est empirée lors du levé de la chaise, lors du travail debout immobile, lors de la marche après 5 minutes, en cours de journée ainsi que le soir. Le patient est amélioré assis, allongé, en dormant et le matin. Il est sous anti-inflammatoires depuis 3 semaines.</p>

   <h3>EXAMEN PHYSIQUE :</h3>
    <p>Le patient est assis en cyphose dorso-lombaire sur la table avec une douleur constante dans le genou droit, la correction posturale n’a aucun effet.</p>
    <p>Debout, le patient présente les même symptômes dans le genou à 4/10 sur l’EVA, les amplitudes de flexion sont sans perte, mais l’extension présente une perte modérée avec douleur augmentée dans le genou. Les glissements latéraux provoquent la douleur dans le genou avec une perte d’amplitude mineure.</p>
    <p>La douleur a augmenté à 6/10 après le test des amplitudes. La répétition debout de flexion (une série de 10) augmente la douleur pendant le mouvement, pas empirée. La répétition d’extension augmente la douleur dans le genou et produit une douleur sur la face latérale de la cuisse, pas empirée après test. La répétition allongée d’extensions augmente la douleur du genou après une série de 10 mouvements ; après 40 il y a en plus production de douleur dans la cuisse (sans empirer après le test). Dans le plan frontal, la répétition de glissement latéral droit, reproduit les mêmes symptômes que l’extension alors que les glissements gauches sont sans effet.</p>

   <h3>CLASSIFICATION PROVISOIRE :</h3>
    <p>A la fin de l’interrogatoire et de l’examen physique, on peut supposer un dérangement postérieur asymétrique qui reste à confirmer par des mouvements d’extension allongé : au rythme de 10 mouvements toutes les deux heures en surveillant l’évolution de la symptomatologie.</p>
   
   <h3>SEANCE N°2 :</h3>
    <p>La douleur dans le genou droit est de même intensité, mais pendant deux jours la douleur a disparu après avoir augmentée au lendemain de la séance. Le patient avoue ne pas faire les exercices, ni corriger sa posture.</p>
    <p>Lors de la séance, la répétition d’EEP : augmenté pendant et mieux après. On insiste sur les exercices et la correction posturale.</p>
   
   <h3>SEANCE N°3 :</h3>
    <p>La douleur est moins intense la journée et monte à 5/10 sur l’EVA en fin de journée. Au réveil la douleur est à 3/10.</p>
    <p>Lors de la séance on réalise des EEP bassin décalé à gauche : après 60 mouvements on obtient l’abolition de la douleur. On demande alors au patient de réaliser les mouvements à domicile.</p>
   
   <h3>SEANCE N°4 :</h3>
    <p>Absence de douleur, la réduction est réalisée. Le reste de la rééducation va consister à stabiliser la réduction, retrouver la fonctionnalité et prévenir les récurrences.</p>
    
